let i = 1
while(i <= 5)
{
    console.log(i);
    i++
}
//do-while 
/* 
  syntax:-
    do{
       set of instruction
       }
*/
let a = 1
do{
    console.log(a);
    a++
}while(a <= 5)

//for loop
/*
  for(initialization condition; increment/decrement)
  {
  set of instructions
  }
*/

for(let i=1; i<=5; i++)
{
    console.log(i)
}
